// Get all ingredients as a flat list
export const getAllIngredients = (language: 'en' | 'es'): string[] => {
  const allIngredients: string[] = [];
  INGREDIENT_CATEGORIES.forEach(category => {
    category.ingredients.forEach(ing => {
      allIngredients.push(language === 'es' ? ing.es : ing.en);
    });
  });
  return allIngredients.sort((a, b) => a.localeCompare(b));
};

// Get ingredients organized by category
export const getIngredientsByCategory = (language: 'en' | 'es') => {
  return INGREDIENT_CATEGORIES.map(category => ({
    id: category.id,
    name: language === 'es' ? category.nameEs : category.nameEn,
    ingredients: category.ingredients.map(ing => 
      language === 'es' ? ing.es : ing.en
    ),
  }));
};

// Search ingredients
export const searchIngredients = (query: string, language: 'en' | 'es'): string[] => {
  if (!query.trim()) return [];
  const normalizedQuery = query.toLowerCase().trim();
  const results: string[] = [];
  
  INGREDIENT_CATEGORIES.forEach(category => {
    category.ingredients.forEach(ing => {
      const name = language === 'es' ? ing.es : ing.en;
      if (name.toLowerCase().includes(normalizedQuery)) {
        results.push(name);
      }
    });
  });
  
  return results.sort((a, b) => a.localeCompare(b));
};

// Get total count
export const getTotalIngredientsCount = (): number => {
  return INGREDIENT_CATEGORIES.reduce((sum, cat) => sum + cat.ingredients.length, 0);
};
